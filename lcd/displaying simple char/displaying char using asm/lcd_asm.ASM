ORG 000H;
;SETTING RS,RW,ENABLE PIN TO RESPECTIVE PINS OF PORT 2

RS EQU P2.0;
RW EQU P2.1;
E EQU P2.2;


;SETTING UP LCD DISPLAY 2*16(5*7 MATRIX)
MOV   P1,#38H;
ACALL  SEND_COMMAND;

;DISPLAY ON CURSOR BLINK
MOV   P1,#0EH;
ACALL SEND_COMMAND;


;CLEAR DISPLAY SCREEN COMMAND
MOV   P1,#01H;
ACALL SEND_COMMAND;


;SENDING THE CHARACTER FOR DISPLAY

LOOP1:
MOV   P1,#080H;FORCING CURSOR TO DISPLAY ON FIRST LINE
ACALL SEND_COMMAND;
;PRINTING CHARACTER ON FIRST LINE OF LCD 
MOV   P1,#'F';
ACALL SEND_DATA;

MOV   P1,#'U';
ACALL SEND_DATA;

MOV   P1,#'C';
ACALL SEND_DATA;

MOV   P1,#'K';
ACALL SEND_DATA;

MOV   P1,#' ';
ACALL SEND_DATA;

MOV   P1,#'O';
ACALL SEND_DATA;

MOV   P1,#'F';
ACALL SEND_DATA;

MOV   P1,#'F';
ACALL SEND_DATA;

MOV   P1,#0C0H;FORCING CURSOR TO DISPLAY ON SECOND LINE
ACALL SEND_COMMAND;

;DISPLAYING CHARACTERS ON SECOND LINE

MOV   P1,#'H';
ACALL SEND_DATA;

MOV   P1,#'S';
ACALL SEND_DATA;

MOV   P1,#'P';
ACALL SEND_DATA;

JMP LOOP1;


;DEFINING SUBROUTINES
SEND_COMMAND:
CLR   RS;TO SEND DATA TO COMMAND REGISTER
CLR   RW;TO ENABLE WRITING TO LCD
SETB  E;;TO LOAD THE DATA TO THE REGISTER
ACALL DELAY;
CLR   E;
RET;


SEND_DATA:
SETB   RS;TO SEND DATA TO DATA REGISTER
CLR   RW;TO ENABLE WRITING TO LCD
SETB  E;TO LOAD THE DATA TO THE REGISTER
ACALL DELAY;
CLR   E;
RET;

DELAY:
LOOP2:MOV R0,#10H;10MS DELAY
LOOP3:MOV R1,#255;
HERE: DJNZ R1,HERE;
DJNZ R0,LOOP3;
RET;

END;
